    private void botao_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_salvarActionPerformed
        String codigo = texto_codigo_aluno.getText();
        String nome = texto_nome.getText();
        String curso = texto_curso.getText();
        int ano = Integer.parseInt(texto_ano.getText());
        Bibliotec.cadastrarAluno(codigo, nome, curso, ano);
        this.dispose();
    }//GEN-LAST:event_botao_salvarActionPerformed

