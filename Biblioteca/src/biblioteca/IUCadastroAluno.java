    private void texto_codigo_alunoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_texto_codigo_alunoFocusLost
            String codigo = texto_codigo_aluno.getText();
        Usuario usuario = Bibliotec.buscaUsuario(codigo);
        if(usuario != null){
            texto_nome.setText(usuario.getNome());
            texto_curso.setText(usuario.getCurso());
            texto_ano.setText(Integer.toString(usuario.getAno()));
            botao_salvar.setEnabled(false);
            this.msg_aviso1.setVisible(true);
        }
        else{
            texto_nome.setText("");
            texto_curso.setText("");
            texto_ano.setText("");
            botao_salvar.setEnabled(true);
            this.msg_aviso1.setVisible(false);
        } 
    }//GEN-LAST:event_texto_codigo_alunoFocusLost

