    private void botao_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_salvarActionPerformed
        // TODO add your handling code here:
        String codigo = texto_codigo_livro.getText();
        String nome = texto_nome.getText();
        int ano = Integer.parseInt(texto_ano.getText());
        Bibliotec.cadastrarLivro(codigo, nome, ano);
        this.dispose();
    }//GEN-LAST:event_botao_salvarActionPerformed

    private void texto_codigo_livroFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_texto_codigo_livroFocusLost
        String codigo = texto_codigo_livro.getText();
        Livro livro = Bibliotec.buscaLivro(codigo);
        if(livro != null){
            texto_nome.setText(livro.getNome());
            texto_ano.setText(Integer.toString(livro.getAno()));
            botao_salvar.setEnabled(false);
            this.msg_aviso1.setVisible(true);
        }
        else{
         texto_nome.setText("");
         texto_ano.setText("");
         botao_salvar.setEnabled(true);
         this.msg_aviso1.setVisible(false);
        }
    }//GEN-LAST:event_texto_codigo_livroFocusLost

